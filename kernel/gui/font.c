/* Characters 0x7F–0xFF filled with zeros (handled by initializer) */

void font_drawchar(int x, int y, char c, uint32_t fg, uint32_t bg, int transp) {
    uint8_t idx = (uint8_t)c;
    const uint8_t *glyph = font_data[idx];
    for (int row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_W; col++) {
            if (bits & (0x80 >> col))
                fb_pixel(x + col, y + row, fg);
            else if (!transp)
                fb_pixel(x + col, y + row, bg);
        }
    }
}

int font_drawstr(int x, int y, const char *s, uint32_t fg, uint32_t bg, int transp) {
    int cx = x;
    while (*s) {
        if (*s == '\n') { cx = x; y += FONT_H; }
        else { font_drawchar(cx, y, *s, fg, bg, transp); cx += FONT_W; }
        s++;
    }
    return cx;
}

int font_drawstr_scaled(int x, int y, const char *s,
                         uint32_t fg, uint32_t bg, int transp, int scale) {
    if (scale <= 1) return font_drawstr(x, y, s, fg, bg, transp);
    int cx = x;
    while (*s) {
        if (*s == '\n') { cx = x; y += FONT_H * scale; }
        else {
            uint8_t idx = (uint8_t)*s;
            const uint8_t *glyph = font_data[idx];
            for (int row = 0; row < FONT_H; row++) {
                uint8_t bits = glyph[row];
                for (int col = 0; col < FONT_W; col++) {
                    uint32_t col_val = (bits & (0x80>>col)) ? fg : bg;
                    if ((bits & (0x80>>col)) || !transp)
                        fb_rect(cx + col*scale, y + row*scale, scale, scale, col_val);
                }
            }
            cx += FONT_W * scale;
        }
        s++;
    }
    return cx;
}
